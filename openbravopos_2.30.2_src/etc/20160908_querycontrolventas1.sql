SELECT P.* FROM RECEIPTS R
LEFT JOIN PAYMENTS P
ON P.RECEIPT = R.ID
LEFT JOIN TICKETS T
ON R.ID = T.ID
LEFT JOIN TICKETLINES TL
ON T.ID = TL.TICKET
LEFT JOIN CUSTOMERS c
on T.CUSTOMER = c.ID
WHERE c.NAME = 'carloscol'
ORDER BY R.DATENEW DESC, TL.LINE DESC


